//De igualdad simple ==

console.log(5 == 5);
console.log("hola" == "HOLA");

//De igualdad estricta === el valor es igual y el tipo de dato son los mismos

console.log(12 === 12);
console.log(12 === "12");

//Mayor que >
console.log(12 > 20);

//Mayor o igual que >=
console.log(12 >= 2);

//Menor que <
console.log(12 < 20);

//Menor o igual que <=
console.log(12 <= 2);

//Distinto que simple !=, el valor es distinto
console.log("hola" != "HOLA");

//Distinto que simple !==, el valor es distinto y el tipo de dato
console.log(45 !== "45");

